<script>
	import wPicker from "@/components/w-picker/w-picker.vue";
    import xiaEditor from '@/components/xia-editor/xia-editor';
    import multipleSelect from "@/components/momo-multipleSelect/momo-multipleSelect";
	export default {
		data() {
			return {
				cross:'',
				ruleForm: {
				riqi: '',
				zhuangtai: '',
				tizhong: '',
				xueya: '',
				xuezhi: '',
				xuetang: '',
				xinlv: '',
				yonghuzhanghao: '',
				userid: '',
				},
				zhuangtaiOptions: [],
				zhuangtaiIndex: 0,
				// 登录用户信息
				user: {},
                ro:{
                   riqi : false,
                   zhuangtai : false,
                   tizhong : false,
                   xueya : false,
                   xuezhi : false,
                   xuetang : false,
                   xinlv : false,
                   yonghuzhanghao : false,
                   userid : false,
                },
			}
		},
		components: {
			wPicker,
            xiaEditor,
            multipleSelect,
		},
		computed: {
			baseUrl() {
				return this.$base.url;
			},



		},
		async onLoad(options) {
			let table = uni.getStorageSync("nowTable");
			// 获取用户信息
			let res = await this.$api.session(table);
			this.user = res.data;
			
			// ss读取
			this.ruleForm.yonghuzhanghao = this.user.yonghuzhanghao
			this.ro.yonghuzhanghao = true;


			// 自定义下拉框值
			this.zhuangtaiOptions = "良好,一般,差".split(',')

			// 如果有登录，获取登录后保存的userid
			this.ruleForm.userid = uni.getStorageSync("userid")
			if (options.refid) {
				// 如果上一级页面传递了refid，获取改refid数据信息
				this.ruleForm.refid = options.refid;
				this.ruleForm.nickname = uni.getStorageSync("nickname");
			}
			// 如果是更新操作
			if (options.id) {
				this.ruleForm.id = options.id;
				// 获取信息
				res = await this.$api.info(`jiankangxinxi`, this.ruleForm.id);
				this.ruleForm = res.data;
			}
			// 跨表
			this.cross = options.cross;
			if(options.cross){
				var obj = uni.getStorageSync('crossObj');
				for (var o in obj){
					if(o=='riqi'){
					this.ruleForm.riqi = obj[o];
					this.ro.riqi = true;
					continue;
					}
					if(o=='zhuangtai'){
					this.ruleForm.zhuangtai = obj[o];
					this.ro.zhuangtai = true;
					continue;
					}
					if(o=='tizhong'){
					this.ruleForm.tizhong = obj[o];
					this.ro.tizhong = true;
					continue;
					}
					if(o=='xueya'){
					this.ruleForm.xueya = obj[o];
					this.ro.xueya = true;
					continue;
					}
					if(o=='xuezhi'){
					this.ruleForm.xuezhi = obj[o];
					this.ro.xuezhi = true;
					continue;
					}
					if(o=='xuetang'){
					this.ruleForm.xuetang = obj[o];
					this.ro.xuetang = true;
					continue;
					}
					if(o=='xinlv'){
					this.ruleForm.xinlv = obj[o];
					this.ro.xinlv = true;
					continue;
					}
					if(o=='yonghuzhanghao'){
					this.ruleForm.yonghuzhanghao = obj[o];
					this.ro.yonghuzhanghao = true;
					continue;
					}
